// Create a function that:

// Takes one number

// Returns its square

// Define a function named 'square' that takes one parameter: num
function square(num) {
    // Return the square of the number (num * num)
    return num * num;
}
// Call the 'square' function with argument 5 and log the result
console.log(square(5));

// Arrow Function Example
// Define a constant 'square1' as an arrow function that returns the square of num
const square1 = (num) => num * num;
// Call the arrow function 'square1' and log the result
console.log(square1(5));