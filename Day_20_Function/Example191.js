// example introduce me function
// Define a function named 'intro' that takes three parameters: name, age, and job
function intro(name, age, job) {
    // Log a formatted string to the console using template literals
    console.log(`Hello my name is ${name} and I am ${age} years old and I am a ${job}`);
}
// Call the 'intro' function with specific arguments
intro("Ravi", 25, "Software Engineer");

// Arrow Function Example
// Define a constant 'intro1' which is an arrow function taking name, age, and job
const intro1 = (name, age, job) => {
    // Log a formatted string to the console using template literals
    console.log(`Hello my name is ${name} and I am ${age} years old and I am a ${job}`);
};
// Call the arrow function 'intro1' with specific arguments
intro1("Ravi", 25, "Software Engineer");

