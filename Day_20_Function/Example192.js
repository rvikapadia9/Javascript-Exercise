// Add Two Numbers

// Create a function that:

// Takes two numbers as parameters

// Returns their sum

// Define a function named 'add' that takes two parameters: a and b
function add(a, b) {
    // Return the sum of a and b
    return a + b;
}
// Call the 'add' function with arguments 10 and 20, and log the result
console.log(add(10, 20));

// Arrow Function Example
// Define a constant 'add1' as an arrow function that returns the sum of a and b
const add1 = (a, b) => a + b;
// Call the arrow function 'add1' and log the result
console.log(add1(10, 20));   


