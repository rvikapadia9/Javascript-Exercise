// Using the same string:

// Get the character at index 2 using two different ways

// Concepts:
// // charAt() vs bracket notation
"use strict";   
let sampleString = "JavaScript";
console.log("Character at index 2 using charAt():", sampleString.charAt(2));
console.log("Character at index 2 using bracket notation:", sampleString[2]);
// Expected Output:
// Character at index 2 using charAt(): v
// Character at index 2 using bracket notation: v
// Explanation:
// In JavaScript, both charAt() method and bracket notation can be used to access characters in a string.
// They provide the same result, but charAt() is a method call while bracket notation is more concise.

                        