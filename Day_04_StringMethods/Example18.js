// Concepts:
// toUpperCase()
// toLowerCase()
"use strict";
let text = "JavaScript is Fun!";
console.log("Original String:", text);  
console.log("Uppercase:", text.toUpperCase());
console.log("Lowercase:", text.toLowerCase());
// Expected Output:
// Original String: JavaScript is Fun!
// Uppercase: JAVASCRIPT IS FUN!
// Lowercase: javascript is fun!
// Explanation:
// The toUpperCase() method converts all characters in a string to uppercase.
// The toLowerCase() method converts all characters in a string to lowercase.