// Exponentiation Operator (**)

// Calculate:

// 2 raised to the power of 5

// 10 raised to the power of 3

let base1 = 2;
let exponent1 = 5;
let result1 = base1 ** exponent1;
console.log(base1, "raised to the power of", exponent1, "is:", result1);   
let base2 = 10;
let exponent2 = 3;
let result2 = base2 ** exponent2;
console.log(base2, "raised to the power of", exponent2, "is:", result2);
// Explanation:
// The exponentiation operator (**) raises the base to the power of the exponent.
// In this example, we calculate 2^5 and 10^3 using the ** operator.
    