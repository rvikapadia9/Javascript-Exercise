// Calculate the area of a circle.
let radius = 7;
let area = Math.PI * radius * radius;
console.log("The area of a circle with radius", radius, "is:", area);

new_area=Math.round(area);

console.log("The rounded area is:", new_area);
//explaination: This code snippet demonstrates how to use the Math.PI property in JavaScript to calculate the area of a circle given its radius. The formula for the area of a circle is A = πr², where r is the radius. The code calculates the area using Math.PI for the value of π and then rounds the result using Math.round() before printing both the exact and rounded areas to the console.
