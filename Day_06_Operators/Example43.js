// Exercise:
// Allow checkout only if cart items are less than or equal to 10.

// Operators: <=
let cartItems = 12;
if (cartItems <= 10) {
    console.log("Checkout allowed.");   
}
else {
    console.log("Checkout denied. Too many items in the cart.");
}
// Explanation: This code checks if the number of items in the cart is less than or equal to 10. If it is, the checkout process is allowed; otherwise, it is denied with a message indicating that there are too many items in the cart. The result is printed to the console.