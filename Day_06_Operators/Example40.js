// Exercise:
// Check if score is between 50 and 100.

// Operators: >=, <=, &&
let score = 65;
if (score >= 50 && score <= 100) {
    console.log("Score is within the range of 50 to 100.");
}
else {
    console.log("Score is out of the range of 50 to 100.");
}
// Explanation: This code checks if a given score falls within the range of 50 to 100, inclusive. It uses the greater than or equal to operator (>=) to check if the score is at least 50 and the less than or equal to operator (<=) to check if the score is at most 100. The logical AND operator (&&) ensures that both conditions must be true for the score to be considered within the specified range. The result is printed to the console.
