// Remainder Operator (%)

// Given:

// let number = 17;


// Find whether the number is even or odd

// Concept: %

let number = 17;
let remainder = number % 2;     
if (remainder === 0) {
    console.log(number, "is an even number.");
} else {
    console.log(number, "is an odd number.");
}   
// Explanation:
// The remainder operator (%) returns the remainder left over when one operand
// is divided by a second operand. Here, we use it to determine if a number is
// even or odd by checking the remainder when divided by 2. If the remainder is
// 0, the number is even; otherwise, it is odd.
    