// Find the cube using Math.pow()

// Concepts:
// Math.sqrt()
// Math.pow()

let value = 64;
let squareRoot = Math.sqrt(value);
console.log("The square root of", value, "is:", squareRoot);
let cube = Math.pow(squareRoot, 3);
console.log("The cube of",squareRoot, "is:", cube);
// Explanation: This code  demonstrates the use of JavaScript's Math methods to perform mathematical calculations. It calculates the square root of a given number using Math.sqrt() and computes the cube of the same number using Math.pow(). The results are then printed to the console.
