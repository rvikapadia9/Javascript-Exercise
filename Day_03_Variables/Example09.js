"use strict";

// Create:
// - a const variable country
// - a let variable score
// - a var variable temperature

const country = "USA";
let score = 0;
var temperature = 72;

// Print all variables to the console
console.log("Country:", country);
console.log("Score:", score);
console.log("Temperature:", temperature);

// Update the let variable score
score = 10;
temperature=75;
// Print updated score to the console
console.log("Updated Score:", score);
console.log("Updated temperature:", temperature);
// Print the type of each variable using typeof
console.log("Type of country:", typeof country);       // string
console.log("Type of score:", typeof score);           // number
console.log("Type of temperature:", typeof temperature); // number
