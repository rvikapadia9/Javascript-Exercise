///For loop examples
let users = ["Admin", "Manager", "User"];
console.log(users);
console.log(users.length);
console.log("Program Started");
for (let i = 0; i < users.length; i++) {
    console.log(users[i]);
}
console.log("Program Ended");
//explanation:
// Initialize 'i' to 0
// Logic: We start the loop from the first element (index 0).
// Condition: i < users.length
// Logic: The loop will run as long as i is less than the length of the users array (3 in this case).
// Increment 'i' by 1
// Logic: After each iteration, we move to the next element in the array.

