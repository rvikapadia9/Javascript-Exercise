// Else → "Unsupported Browser"
let browser = "chrome"; 
if (browser === "chrome") {
    console.log("Run Chrome Tests");
}
else if (browser === "firefox") {
    console.log("Run Firefox Tests");
}
else if (browser === "edge") {
    console.log("Run Edge Tests");
}
else {
    console.log("Unsupported Browser");
}


    