// Exercise:

// "red" → "Stop"

// "yellow" → "Get Ready"

// "green" → "Go"

// Else → "Invalid Signal"
let light = "yellow"; // You can change this value to test different cases
if (light === "red") {
    console.log("Stop");
}
else if (light === "yellow") {  
    console.log("Get Ready");
}
else if (light === "green") {
    console.log("Go");
}
else {
    console.log("Invalid Signal");
}
// Example Outputs:
// For light = "red", Output: Stop
// For light = "yellow", Output: Get Ready